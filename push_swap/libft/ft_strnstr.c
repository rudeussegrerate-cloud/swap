#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	char	*cast_big;

	cast_big = (char *)big;
	i = 0;
	if (*little == '\0')
		return (cast_big);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && (i + j) < len)
		{
			j++;
			if (little[j] == '\0')
				return (&cast_big[i]);
		}
		i++;
	}
	return (NULL);
}
