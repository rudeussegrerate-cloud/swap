#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	digit;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		n = -n;
	}
	if (n / 10 != 0)
		ft_putnbr_fd(n / 10, fd);
	digit = (n % 10) + 48;
	write(fd, &digit, 1);
}
